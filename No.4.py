# Median calculator
user_input1 = int(input("ENTER THE 1ST PARAMETER"))
user_input2 = int(input("ENTER THE 2ND PARAMETER"))
user_input3 = int(input("ENTER THE 3RD PARAMETER"))
sum_of_parameters = user_input1 + user_input2 + user_input3
median_formular = sum_of_parameters / 2
print(median_formular)


